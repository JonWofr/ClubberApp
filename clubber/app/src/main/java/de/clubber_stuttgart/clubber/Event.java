package de.clubber_stuttgart.clubber;

public class Event {
    String dte;
    String name;
    String club;
    String adrs;
    String srttime;
    String endtime;
    String price;
    String soldas;
    String buttonLink;
    String genre;
    String occ;


    Event (String dte, String name, String club, String adrs, String srttime, String endtime, String price, String soldas, String buttonLink, String genre, String occ){
        this.dte = dte;
        this.name = name;
        this. club = club;
        this.adrs = adrs;
        this.srttime = srttime;
        this.endtime = endtime;
        this.price = price;
        this.soldas = soldas;
        this.buttonLink = buttonLink;
        this.genre = genre;
        this.occ = occ;
    }
}
